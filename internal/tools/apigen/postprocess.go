package main

import (
	"fmt"
	"go/format"
	"regexp"
	"strings"
)

// postProcess removes the oapi-codegen runtime dependency from generated code
// and fixes known codegen bugs.
func postProcess(src []byte) ([]byte, error) {
	s := string(src)

	s = fixPackageComment(s)
	s = removeMergeMethods(s)
	s = removeRuntimeImport(s)
	s = fixDiscriminatorPointerAssign(s)

	out, err := format.Source([]byte(s))
	if err != nil {
		return nil, fmt.Errorf("formatting post-processed source: %w", err)
	}
	return out, nil
}

// packageCommentRe matches the oapi-codegen package doc comment block and
// replaces it with a standalone generated-code marker that won't appear in
// godoc.
var packageCommentRe = regexp.MustCompile(
	`(?s)^// Package \w+ provides primitives.*?DO NOT EDIT\.\npackage (\w+)`,
)

func fixPackageComment(s string) string {
	return packageCommentRe.ReplaceAllString(s,
		"// Code generated by oapi-codegen. DO NOT EDIT.\n\npackage $1")
}

// mergeFuncRe matches entire Merge* methods including the preceding doc comment
// through the closing brace.
var mergeFuncRe = regexp.MustCompile(
	`(?ms)^// Merge\w+ performs a merge[^\n]*\n.+?\n\}\n`,
)

func removeMergeMethods(s string) string {
	return mergeFuncRe.ReplaceAllString(s, "")
}

func removeRuntimeImport(s string) string {
	s = strings.ReplaceAll(s, "\t\"github.com/oapi-codegen/runtime\"\n", "")
	s = strings.ReplaceAll(s, "\topenapi_types \"github.com/oapi-codegen/runtime/types\"\n", "")
	return s
}

// discriminatorRe matches discriminator assignments in From* methods
// (oapi-codegen#2297): v.Field = "literal" where Field is actually *string.
// We wrap with a temporary variable to take the address.
var discriminatorRe = regexp.MustCompile(
	`(func \([^)]+\) From\w+\([^)]*\) error \{\n)\t(\w+\.\w+) = (".*")`,
)

func fixDiscriminatorPointerAssign(s string) string {
	return discriminatorRe.ReplaceAllString(s, "${1}\t_v := $3\n\t$2 = &_v")
}

// modelConfigHeader is the doc comment prepended to the generated modelconfig
// file, mirroring the doc comments produced by clientgen for the API packages.
const modelConfigHeader = `// Code generated by github.com/atombender/go-jsonschema. DO NOT EDIT.

// Package modelconfig contains generated types for the Truss model
// configuration schema.
//
// Types in this package are generated from the Truss config JSON Schema and
// are NOT covered by any stability or compatibility guarantees. They may
// change without notice between versions.
package modelconfig
`

var modelConfigHeaderRe = regexp.MustCompile(
	`(?s)^// Code generated by github.com/atombender/go-jsonschema.*?\npackage modelconfig\n`,
)

func postProcessModelConfig(src []byte) ([]byte, error) {
	src = modelConfigHeaderRe.ReplaceAll(src, []byte(modelConfigHeader))
	out, err := format.Source(src)
	if err != nil {
		return nil, fmt.Errorf("formatting modelconfig source: %w", err)
	}
	return out, nil
}
